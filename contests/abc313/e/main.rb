N = gets.to_i
S = gets.chomp
MOD = 998244353

if S.match(/[2-9][2-9]/)
    puts -1
    exit
end

s = S.split('').map(&:to_i)
res = 0

while(l = s.pop) do
    break if s.size == 0
    if l == 1
        res = res + 1 % MOD
    else
        res = res + 1 + (l-1) * (res+1) % MOD
    end
    # warn "l:#{l} res:#{res} s:#{s}"
end

puts res

